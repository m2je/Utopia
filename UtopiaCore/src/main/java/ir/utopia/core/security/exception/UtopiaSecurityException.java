package ir.utopia.core.security.exception;

import ir.utopia.core.exception.CoreException;

public class UtopiaSecurityException extends CoreException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 9082588659793935399L;

	public UtopiaSecurityException() {
		super();
	}

	public UtopiaSecurityException(String message, Throwable cause) {
		super(message, cause);
	}

	public UtopiaSecurityException(String message) {
		super(message);
	}

	public UtopiaSecurityException(Throwable cause) {
		super(cause);
	}

}
